
public class TripInfo {
private String Origin;
private String Destination;
private double Price;
private int TripNo;
private String InternalExternal;
private String TripFlavor;
private String Date;
private String LiscenceNumber;
private String DriverID;
private String Time;

//internal or external
public String getDate() {
	return Date;
}
public void setDate(String date) {
	Date = date;
}
public String getOrigin() {
	return Origin;
}
public void setOrigin(String origin) {
	Origin = origin;
}
public String getDestination() {
	return Destination;
}
public void setDestination(String destination) {
	Destination = destination;
}
public double getPrice() {
	return Price;
}
public void setPrice(double price) {
	Price = price;
}
public int getTripNo() {
	return TripNo;
}
public void setTripNo(int tripNo) {
	TripNo = tripNo;
}
public String getLiscenceNumber() {
	return LiscenceNumber;
}
public void setLiscenceNumber(String liscenceNumber) {
	LiscenceNumber = liscenceNumber;
}
public String getDriverID() {
	return DriverID;
}
public void setDriverID(String driverId) {
	DriverID = driverId;
}
public String getInternalExternal() {
	return InternalExternal;
}
public void setInternalExternal(String internalExternal) {
	InternalExternal = internalExternal;
}
public String getTripFlavor() {
	return TripFlavor;
}
public void setTripFlavor(String tripFlavor) {
	TripFlavor = tripFlavor;
}
public String getTime() {
	return Time;
}
public void setTime(String time) {
	Time = time;
}



}



